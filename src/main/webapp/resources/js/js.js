function showList() {
    let rolling_content = document.querySelector('.rolling_content');
    let rolling = document.querySelector('.rolling');
	
    rolling.style.display = 'none';
    rolling_content.style.display = 'block';
}
    
function hideList() {
    let rolling_content = document.querySelector('.rolling_content');
    let rolling = document.querySelector('.rolling');
	
    rolling.style.display = 'block';
    rolling_content.style.display = 'none';
}


	
